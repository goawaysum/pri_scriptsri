#!/bin/bash
menu(){
cat <<EOF
	echo "##################    菜单     #####################"
	echo "##################   1、吃饭   #####################"
	echo "##################   2、吃面   #####################"
	echo "##################   3、喝粥   #####################"
	echo "##################   4、喝汤   #####################"
EOF
}
menu
num=$((RANDOM%4+1))
case $num in
	1)
		echo "1、吃饭"
		;;
	2)
		echo "2、吃面"
		;;
	3)
		echo "3、喝粥"
		;;
	4)
		echo "4、喝汤"
esac
		



